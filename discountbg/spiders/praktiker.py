    def parse(self, response):
        base_url = 'https://praktiker.bg/'
        products = response.xpath("//span[contains(@class, 'product-price--old')]/ancestor::div[contains(@class, 'product-item')]//h2[contains(@class, 'product-item__title')]/a/@href")
        next_page = response.xpath("//a[@class='pagination__anchor pagination__anchor--last']/@href").get()
        
        for product in products:
            product_url = base_url + product.get()
            yield response.follow(product_url, callback = self.scrape_product)

        if next_page:
            url = base_url + next_page
            yield response.follow(url, callback = self.parse)
        
    
    def scrape_product(self, response):
        old_price_text = response.xpath("normalize-space(//span[@class='product-price product-price--old']//span[@class='product-price__value']/text())").get()
        old_price = int(old_price_text) + 1
        old_price_string = str(old_price)

        new_price_text = response.xpath("normalize-space(//span[@class='product-price']//span[@class='product-price__value']/text())").get()
        new_price = int(new_price_text) + 1
        new_price_string = str(new_price)

        discount_percent = round(((old_price - new_price) / old_price) * 100)
        discount_percent_string = str(discount_percent)

        images = response.xpath("//swiper[@class='slider pdp-slider swiper']//img/@src")
